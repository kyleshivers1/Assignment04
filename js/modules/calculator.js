// ADD FOUR PUBLIC FUNCTIONS BELOW
// ADD FUNCTION
const addNums = (userNumberOne, userNumberTwo) => {
  return userNumberOne + userNumberTwo;
};

// SUBTRACT FUNCTION
const subNums = (userNumberOne, userNumberTwo) => {
  return userNumberOne - userNumberTwo;
};

// MULTIPLY FUNCTION
const multNums = (userNumberOne, userNumberTwo) => {
  return userNumberOne * userNumberTwo;
};

// DIVIDE FUNCTION
const divNums = (userNumberOne, userNumberTwo) => {
  return userNumberOne / userNumberTwo;
};

// EXPORT THE FOUR PUBLIC FUNCTIONS
export { addNums, subNums, multNums, divNums };
